package sections

import "fmt"

// chan<- incomming channel param
// <- outgoing channel param
func ping(pings chan<- string, msg string) {
	pings <- msg
}

func pong(pings <-chan string, pongs chan<- string) {
	msg := <-pings // instanciating a new input stream
	pongs <- msg
}

func Directions() {
	pings := make(chan string, 1)
	pongs := make(chan string, 1)

	ping(pings, "passed message")
	pong(pings, pongs)

	fmt.Println(<-pongs)
}
